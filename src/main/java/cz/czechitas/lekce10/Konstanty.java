package cz.czechitas.lekce10;

/**
 * Konstanty používané v kalkulačce.
 *
 * @author Filip Jirsák
 */
public class Konstanty {
    private double pi;
    private double e;

    /**
     * Vrátí hodnotu čísla π.
     *
     * @return
     */
    public double getPi() {
        return pi;
    }

    public void setPi(double pi) {
        this.pi = pi;
    }

    /**
     * Vrátí hodnotu Eulerovy konstanty.
     *
     * @return
     */
    public double getE() {
        return e;
    }

    public void setE(double e) {
        this.e = e;
    }
}
